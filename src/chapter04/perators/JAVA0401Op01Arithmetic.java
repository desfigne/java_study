package chapter04.perators;

/*
 * 사칙 연산자 : +, -, *, /, %
 */

public class JAVA0401Op01Arithmetic {

	public static void main(String[] args) {
		int number1 = 5;
		int number2 = 2;

		System.out.println("number1 + number2 = " + (number1 + number2));
		System.out.println("number1 - number2 = " + (number1 - number2));
		System.out.println("number1 * number2 = " + (number1 * number2));
		System.out.println("number1 / number2 = " + (number1 / number2));
		System.out.println("number1 % number2 = " + (number1 % number2));
	}

}
